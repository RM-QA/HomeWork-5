package org.example;

public class MathUtils {
    public int add(int a, int b) {

        return a + b;
    }


    public Boolean bigger(int a, int b) {
        if (a > b) {
            return true;
        } else {
            return false;
        }
    }


    // homework 7

    public float divide(float a, float b) {
        return a / b;
    }

    public float multiply(float a, float b) {
        return a * b;
    }

    public int square(int a) {
        return a * a;
    }
}

